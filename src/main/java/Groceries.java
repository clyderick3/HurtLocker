public class Groceries {

    private String name;
    private float price;
    private String type;
    private String expirationDate;

    public Groceries(String name, float price, String type, String expirationDate){
        this.name = name;
        this.price = price;
        this.type = type;
        this.expirationDate = expirationDate;

    }
}
